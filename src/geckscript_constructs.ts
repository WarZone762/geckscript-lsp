type ObjectMap = {
  [key: string]: boolean;
}

function ObjectMapToLower(upper: ObjectMap): ObjectMap {
  const lower: ObjectMap = {};

  for (const entry in upper) {
    lower[entry.toLowerCase()] = true;
  }

  return lower;
}

